#include <stdio.h>
#include <math.h>

int main() {
    // Math Functions

    // Square root
    double A = sqrt(9);
    printf("%lf\n", A);

    // Power of
    double B = pow(2, 4);
    printf("%lf\n", B);

    // Round off
    int C = round(3.14);
    printf("%d\n", C);

    // Round up
    int D = ceil(3.14);
    printf("%d\n", D);

    // Round down
    int E = floor(3.14);
    printf("%d\n", E);

    // Log
    double G = log(3);
    printf("%lf\n", G);

    // SIN
    double H = sin(45);
    printf("%lf\n", H);

    // COS
    double I = cos(45);
    printf("%lf\n", I);

    // TAN
    double J = tan(45);
    printf("%lf\n", J);

    return 0;
}